print ("Введите длину прямоугольника в мм")
lenght = float(input())

print ("Введите ширину прямоугольника в мм")
width = float(input())
 

area = width * lenght
perimeter = 2 * (width + lenght)

print ("Площадь прямоугольника равна -", area, "мм².")
print ("Периметр прямоугольника равен -", perimeter,"мм." )